int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 1;
	if (*str == '\0')
		i = 1;
	while (*str != '\0')
	{
		if (!((*str >= 'A') && (*str <= 'Z')))
		{
			if (!((*str >= 'a') && (*str <= 'z')))
			{
				i = 0;
			}
		}
		str++;
	}
	return (i);
}
