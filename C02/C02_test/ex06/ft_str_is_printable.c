int	ft_str_is_printable(char *str)
{
	int	i;

	i = 1;
	if (*str == '\0')
		i = 1;
	while (*str != '\0')
	{
		if (!(((*str >= ' ') && (*str <= '~'))))
		{
			i = 0;
		}
		str++;
	}
	return (i);
}
