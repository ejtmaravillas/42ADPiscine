int	ft_strcmp(char *s1, char *s2)
{
	int	a;

	a = 0;
	while ((*s1 != '\0') || (*s2 != '\0'))
	{
		if (*s1 > *s2)
		{
			a = *s1 - *s2;
			break ;
		}
		else if (*s1 < *s2)
		{
			a = *s1 - *s2;
			break ;
		}
		else
		{
			s1++;
			s2++;
		}
	}
	return (a);
}
